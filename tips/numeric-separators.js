/**
 * For readability, you can separate numbers by `_`
 *
 * @reference https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Lexical_grammar
 */

const BIG_NUMBER = 1000000;

// It's easier to read with separators
const MILLION = 1_000_000;

// It's still an int
typeof MILLION; // "number"

// Also works with floats
const MILLION_POINT_ONE = 1_000_000.01;
